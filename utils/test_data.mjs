// Return a partial function that calculates the rate size in bits for a given digest size
const rateLength = shift => digestLen => (1600 - (digestLen << 1)) >>> shift
const rateLengthAsBytes = rateLength(3)

const sha3Padding = digestLen => {
  const byteLen = rateLengthAsBytes(digestLen)
  const pad1Byte = 0x61
  const padFirstByte = 0x60
  const padLastByte = 0x01

  return testData => {
    let arr = new Uint8Array(byteLen)
    let encoded = new TextEncoder().encode(testData)

    arr.set(encoded)

    // Insert padding
    let bytesRem = byteLen - encoded.length

    if (bytesRem === 1) {
      arr.set(pad1Byte, encoded.length - 1)
    } else if (bytesRem === 2) {
      arr.set([padFirstByte, padLastByte], encoded.length - 2)
    } else {
      let zeroes = new Uint8Array(bytesRem - 2)
      arr.set([padFirstByte, ...zeroes, padLastByte], encoded.length)
    }

    return arr
  }
}
