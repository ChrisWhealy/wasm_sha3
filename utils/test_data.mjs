const toHexFormat = byteArray => {
  const hex = Array.from(byteArray, b => '0x' + b.toString(16).padStart(2, '0'))
  let output = '[\n'

  for (let i = 0; i < hex.length; i += 8) {
    output += '  ' + hex.slice(i, i + 8).join(', ') + ',\n'
  }

  output += ']'
  return output
}

